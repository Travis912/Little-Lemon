import SeededRandom from "./seededRandom";

export default function FetchAPI(date) {
  let result = [];
  let random = SeededRandom(date.getDate());

  for (let i = 17; i <= 23; i++) {
    if (random() < 0.5) {
      result.push(i + ":00");
    }
    if (random() < 0.5) {
      result.push(i + ":30");
    }
  }
  return result;
}
